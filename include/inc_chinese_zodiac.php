
		<h1>Chinese Zodiac</h1>
		<p>The Chinese zodiac is a mathematical cycle of 12 different animals. Each animal is supposed to represent one year. 
		People are associated with the zodiac animal for the year that they are born. ChineseZodiac.com states that the 12 animals in order are: Rat, Ox, Tiger, 
		Rabbit, Dragon, Snake, Horse, Goat, Monkey, Rooster, Dog and Pig. Zodiac animals can also represent hours of the day and directions. Although it is 
		called the Chinese zodiac, this system is also used in several other countries in Asia such as Korea, Japan, and Vietnam. Some people believe that each 
		zodiac animal has a personality, and people born in the same year may have similar personalities.
		</p>
