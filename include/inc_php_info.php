<h1>The Perks of PHP</h1>
<p> PHP is a server-side scripting language designed primarily for web development but also used as a general-purpose programming language. 
Originally created by Rasmus Lerdorf in 1994, the PHP reference implementation is now produced by The PHP Development Team.
PHP originally stood for Personal Home Page, but it now stands for the recursive acronym PHP: Hypertext Preprocessor.
What distinguishes PHP from something like client-side JavaScript is that the code is executed on the server, generating HTML 
which is then sent to the client. The client would receive the results of running that script, but would not know what the underlying 
code was. You can even configure your web server to process all your HTML files with PHP, and then there's really no way that users can 
tell what you have up your sleeve.</p>
<p>Here are just a couple of things that PHP does:</p>
<ul>
  <li>PHP can generate dynamic page content</li>
  <li>PHP can create, open, read, write, delete, and close files on the server</li>
  <li>PHP can collect form data</li>
  <li>PHP can send and receive cookies</li>
  <li>PHP can add, delete, modify data in your database</li>
  <li>PHP can be used to control user-access</li>
  <li>PHP can encrypt data</li>
</ul>