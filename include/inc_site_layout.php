<h1>Site Layout</h1>
<p> A server-side dynamic web page is a web page whose construction is controlled by an application server 
processing server-side scripts. In server-side scripting, parameters determine how the assembly of every new web page 
proceeds, including the setting up of more client-side processing.</p>
<p>A client-side dynamic web page processes the web page using HTML scripting running in the browser as it loads. 
JavaScript and other scripting languages determine the way the HTML in the received page is parsed into the Document 
Object Model, or DOM, that represents the loaded web page. The same client-side techniques can then dynamically update or 
change the DOM in the same way.
</p>
